#!/usr/bin/env node

/**
 * Generate Decap CMS config.yml from profiles.js
 *
 * Reads pen name profiles and generates the admin config with
 * blog and book collections for each pen name.
 *
 * Usage:
 *   node scripts/generate-cms-config.js
 */

const fs = require('fs');
const path = require('path');

const profilesPath = path.join(__dirname, '..', 'src', '_data', 'profiles.js');
const outputPath = path.join(__dirname, '..', 'src', 'admin', 'config.yml');

// Clear require cache so we always get fresh data
delete require.cache[require.resolve(profilesPath)];
const { profiles } = require(profilesPath);

const penNames = Object.keys(profiles);

// Build blog collections
const blogCollections = penNames.map(name => {
    const p = profiles[name];
    const folder = `src/${p.blogFolder}`;
    // Derive a short key from the blogFolder (e.g. "blogs/rodthunderen" -> "rodthunderen")
    const key = p.blogFolder.replace('blogs/', '');

    return [
        `  - name: "blog-${key}"`,
        `    label: "${name} - Blog Posts"`,
        `    label_singular: "${name} Blog Post"`,
        `    folder: "${folder}"`,
        `    create: true`,
        `    slug: "{{slug}}"`,
        `    extension: "md"`,
        `    format: "frontmatter"`,
        `    editor:`,
        `      preview: true`,
        `    fields:`,
        `      - {label: "Layout", name: "layout", widget: "hidden", default: "layouts/post.njk"}`,
        `      - {label: "Author", name: "author", widget: "hidden", default: "${name}"}`,
        `      - {label: "Title", name: "title", widget: "string"}`,
        `      - {label: "Description", name: "description", widget: "text", hint: "Brief description for SEO and previews"}`,
        `      - {label: "Publish Date", name: "date", widget: "datetime"}`,
        `      - {label: "Tags", name: "tags", widget: "hidden", default: "post"}`,
        `      - {label: "Body", name: "body", widget: "markdown"}`,
    ].join('\n');
});

// Build book collections
const bookCollections = penNames.map(name => {
    const p = profiles[name];
    const key = p.blogFolder.replace('blogs/', '');
    const bookFile = `src/_data/${p.bookList}`;

    return [
        `  - name: "books-${key}"`,
        `    label: "${name} - Books"`,
        `    files:`,
        `      - name: "${key}-booklist"`,
        `        label: "Book Catalog"`,
        `        file: "${bookFile}"`,
        `        fields:`,
        `          - label: "Books"`,
        `            name: "books"`,
        `            widget: "list"`,
        `            fields:`,
        `              - {label: "Book Title", name: "name", widget: "string"}`,
        `              - {label: "Series Name", name: "series", widget: "string", required: false}`,
        `              - {label: "Cover Image", name: "image", widget: "image"}`,
        `              - {label: "Buy Link", name: "link", widget: "string", hint: "Amazon, BookShop.org, etc."}`,
        `              - {label: "Description", name: "description", widget: "text", required: false}`,
        `              - {label: "ISBN", name: "isbn", widget: "string", required: false}`,
    ].join('\n');
});

// Site pages collection (static)
const sitePages = [
    `  - name: "pages"`,
    `    label: "Site Pages"`,
    `    description: "Edit main site pages (shared across all pen names)"`,
    `    files:`,
    `      - name: "newsletter"`,
    `        label: "Newsletter Page"`,
    `        file: "src/newsletter.md"`,
    `        fields:`,
    `          - {label: "Layout", name: "layout", widget: "hidden", default: "layouts/base.njk"}`,
    `          - {label: "Title", name: "title", widget: "string"}`,
    `          - {label: "Navigation Order", name: "eleventyNavigation", widget: "object", fields: [{label: "Key", name: "key", widget: "string"}, {label: "Order", name: "order", widget: "number"}]}`,
    `          - {label: "Body", name: "body", widget: "markdown"}`,
].join('\n');

// Assemble full config
const config = `# Auto-generated by scripts/generate-cms-config.js
# Do not edit manually — changes will be overwritten.
# To update, edit src/_data/profiles.js and re-run this script.

# Backend configuration
backend:
  name: git-gateway
  branch: main

# Local backend for development
# To use: run "npm run admin" in a separate terminal
local_backend: true

# Media and public folders
media_folder: "src/images"
public_folder: "/images"

# Collections
collections:
  # ============================================
  # BLOG POSTS
  # ============================================
${blogCollections.join('\n\n')}

  # ============================================
  # BOOKS
  # ============================================
${bookCollections.join('\n\n')}

  # ============================================
  # SITE PAGES
  # ============================================
${sitePages}
`;

fs.writeFileSync(outputPath, config, 'utf8');
console.log(`CMS config generated with ${penNames.length} pen name(s): ${penNames.join(', ')}`);
